package day13;

public class ForLoop1 {
	public static void main(String[] args) {
		
		//             4 <= 3
		for(int i = 0; i <= 3; i++) {
			
			System.out.print(i+" ");
			
		}
		
		System.out.println("\n");
		
		int index = 0;
		while(index <= 10) {
			System.out.print(index+" ");
			index++;
		}
	}
}
