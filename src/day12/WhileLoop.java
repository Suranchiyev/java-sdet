package day12;

public class WhileLoop {
	public static void main(String[] args) {
		
       int count = 0;
       
       //      4
       while(count <= 3) {
    	   System.out.print(count+" ");
    	   count++;
    	   
       }
      
       
       System.out.println();
       System.out.println();
       System.out.println(count);
       
	}
}
