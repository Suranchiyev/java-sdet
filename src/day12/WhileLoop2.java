package day12;

public class WhileLoop2 {
   public static void main(String[] args) {
	  /**
	   *  Print 10 times "Hello World!" by using while loop
	   */
	   
	   int count = 1;
	   //     6  <= 5
	   while(count <= 5) {
		   System.out.println("Hello World!");
		   count++;
	   }
	   System.out.println(count);
   }
}
