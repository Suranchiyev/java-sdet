package day4;

public class Remainder {
    public static void main(String args[]) {

    	System.out.println(213 % 200); // -> 13
    	System.out.println(1133 % 200);// -> 133
    	 
    	System.out.println(77 % 5);    // -> 2
    	System.out.println(15 * 5);  
    	
    	System.out.println(11 % 2);   // -> 1
    //	System.out.println(11 / 2);   // -> 0
    	
    	System.out.println(109 % 10);  // -> 9
    	System.out.println(25 % 20);
    	System.out.println(26 % 5);    // -> 1
    	
    	
    }
}
