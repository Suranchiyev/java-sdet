package day16;

public class DiscoveringMainMethod {
	public static void main(String[] args) {
		// public -> access modifiers (private, protected, default, public)
		// static -> belongs to class
		// void   -> does not return any value
		// main   -> name of the method
		// (String[] args)  -> argument for main method of type String[] array
		
		System.out.println(args[0]);
		System.out.println(args[1]);
		
		System.out.println("Checking main method");
	}
}
