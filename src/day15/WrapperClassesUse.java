package day15;

public class WrapperClassesUse {
	public static void main(String[] args) {
	
		// autoboxing
        Integer num1 = 19;
        System.out.println(num1 + 20);
        
        int number = 29;
        Integer num2 = number;
        
        Character ch = 'h';
        
        // unboxing
        Boolean b = false;
        boolean b1 = b;
        
        
        Integer num3 = 55;
        int num4 = 5;
        Integer num5 = num3 + num4;
        System.out.println(num3 + num4);
        
        
	}
}
